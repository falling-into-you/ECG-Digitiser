#!/bin/bash
# ============================================================
# ECG 数据处理全流程自动化脚本
# 从图像生成 -> 后处理 -> 合并 -> 验证 -> 预处理 -> 训练
# 用法: bash shells/run_all.sh
# ============================================================

set -e

SCRIPT_DIR="$(cd "$(dirname "$0")" && pwd)"
PROJECT_ROOT="$(cd "$SCRIPT_DIR/.." && pwd)"

# 记录开始时间
START_TIME=$(date +%s)
echo "============================================================"
echo "ECG 数据处理全流程"
echo "开始时间: $(date)"
echo "============================================================"

# ============================================================
# Step 1: 生成图像
# ============================================================
echo ""
echo "============================================================"
echo "Step 1/7: 生成 ECG 图像"
echo "============================================================"
bash "$SCRIPT_DIR/batch_generate_image/01_gen_optimized_dataset.sh"

# ============================================================
# Step 2: 后处理 (像素加密 + 转 nnUNet 格式)
# ============================================================
echo ""
echo "============================================================"
echo "Step 2/7: 后处理 (像素加密 + 转 nnUNet 格式)"
echo "============================================================"
bash "$SCRIPT_DIR/batch_generate_image/02_postprocess.sh"

# ============================================================
# Step 3: 合并数据集
# ============================================================
echo ""
echo "============================================================"
echo "Step 3/7: 合并数据集"
echo "============================================================"
bash "$SCRIPT_DIR/batch_generate_image/03_merge_datasets.sh"

# ============================================================
# Step 4: 验证配对
# ============================================================
echo ""
echo "============================================================"
echo "Step 4/7: 验证配对"
echo "============================================================"
bash "$SCRIPT_DIR/batch_generate_image/04_validate_pairs.sh"

# ============================================================
# Step 5: 准备 nnUNet (添加 _0000 后缀)
# ============================================================
echo ""
echo "============================================================"
echo "Step 5/7: 准备 nnUNet (添加 _0000 后缀)"
echo "============================================================"
bash "$SCRIPT_DIR/batch_generate_image/05_prepare_nnunet.sh"

# ============================================================
# Step 6: nnUNet 预处理
# ============================================================
echo ""
echo "============================================================"
echo "Step 6/7: nnUNet 预处理"
echo "============================================================"
bash "$SCRIPT_DIR/train/01_preprocess.sh"

# ============================================================
# Step 7: nnUNet 训练
# ============================================================
echo ""
echo "============================================================"
echo "Step 7/7: nnUNet 训练"
echo "============================================================"
bash "$SCRIPT_DIR/train/02_train.sh"

# ============================================================
# 完成
# ============================================================
END_TIME=$(date +%s)
ELAPSED=$((END_TIME - START_TIME))
HOURS=$((ELAPSED / 3600))
MINUTES=$(((ELAPSED % 3600) / 60))
SECONDS=$((ELAPSED % 60))

echo ""
echo "============================================================"
echo "全流程完成!"
echo "结束时间: $(date)"
echo "总耗时: ${HOURS}h ${MINUTES}m ${SECONDS}s"
echo "============================================================"
